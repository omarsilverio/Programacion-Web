				<div class="elementor-post__text">
				<h3 class="elementor-post__title">
					<label>
						Educación no formal    </label>
				</h3>
				<div class="elementor-post__excerpt">
					<p>Abarca toda actividad educativa organizada y
sostenida que no corresponda exactamente a la
definición de educación formal. Por lo tanto, la
educación no formal se puede impartir dentro
o fuera de un establecimiento docente y está
destinada a personas de cualquier edad. Según
los distintos contextos nacionales, puede abarcar
programas de alfabetización de adultos, educación
